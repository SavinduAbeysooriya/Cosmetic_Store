        if (usernameTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "username is Empty", "Alert", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if (passwordTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "password is Empty", "Alert", JOptionPane.INFORMATION_MESSAGE);
            } else {
                int choice = JOptionPane.showConfirmDialog(null, "Do you want to Add Account?", "Confirmation", JOptionPane.OK_CANCEL_OPTION);
        
                if (choice == JOptionPane.OK_OPTION) {
                    Object selectedItem = accountTypeCB.getSelectedItem();
                    String selectedValue = null;
                    if (selectedItem != null) {
                        selectedValue = selectedItem.toString();
                    }
                    Users.increaseLengthUsers(userIdLbl.getText(),usernameTxt.getText(),passwordTxt.getText(),selectedValue);
//                    Users.usersDetails[Users.usersDetails.length - 1][0] = userIdLbl.getText();
//                    Users.usersDetails[Users.usersDetails.length - 1][1] = usernameTxt.getText();
//                    Users.usersDetails[Users.usersDetails.length - 1][0] = passwordTxt.getText();
                    dispose();
                    System.out.println(Users.usersDetails[Users.usersDetails.length - 1][1] + "");
                } else if (choice == JOptionPane.CANCEL_OPTION) {
                } else {
                }
            }
        }
        
