    private void setValuesToUserId() {
        String currentUserId = Users.usersDetails[Users.usersDetails.length - 1][0];
        String prefix = currentUserId.substring(0, 1); 
        int number = Integer.parseInt(currentUserId.substring(1));
        userIdLbl.setText(String.format("%s%04d", prefix, number + 1));
    }
